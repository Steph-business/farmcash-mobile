/// Entete de la page de reception d'un lot dans un entrepot cooperative.
/// Bouton retour qui fallback vers le tab Stock s'il n'y a pas
/// d'historique de navigation, et titre fige centre "Receptionner un lot".
class EnteteReceptionLot extends StatelessWidget {
  const EnteteReceptionLot({super.key});

  @override
